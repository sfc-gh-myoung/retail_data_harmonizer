def test_app_log_entry_model():
    """Verify AppLogEntry model validation and serialization."""
    entry = AppLogEntry(
        timestamp="2024-01-15T10:30:00Z",
        level="info",
        message="Test message",
        data={"key": "value"},
    )
    assert entry.timestamp == "2024-01-15T10:30:00Z"
    assert entry.level == "info"
    assert entry.message == "Test message"
    assert entry.data == {"key": "value"}


def test_app_log_entry_optional_data():
    """Verify AppLogEntry with no data field."""
    entry = AppLogEntry(timestamp="2024-01-15T10:30:00Z", level="warn", message="Warning message")
    assert entry.data is None


