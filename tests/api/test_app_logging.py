    log_client_error(report, request_id="req-456")

    entries = buffer.get_recent(limit=1)
    assert len(entries) == 1
    assert entries[0].level == "error"
    assert "Client error: TypeError" in entries[0].message
    assert entries[0].data is not None
    assert entries[0].data["request_id"] == "req-456"
    assert entries[0].data["error_type"] == "TypeError"
    assert entries[0].data["url"] == "https://example.com/app"
