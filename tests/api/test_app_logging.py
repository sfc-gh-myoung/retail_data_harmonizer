def test_log_buffer_basic_logging():
    """Verify basic log entry addition and retrieval."""
    buffer = LogBuffer(max_size=10)
    buffer.log("info", "Test message", {"key": "value"})

    entries = buffer.get_recent(limit=10)
    assert len(entries) == 1
    assert entries[0].level == "info"
    assert entries[0].message == "Test message"
    assert entries[0].data == {"key": "value"}


def test_log_buffer_convenience_methods():
    """Verify convenience methods (debug, info, warn, error)."""
    buffer = LogBuffer(max_size=10)
    buffer.debug("Debug message")
    buffer.info("Info message")
    buffer.warn("Warning message")
    buffer.error("Error message")

    entries = buffer.get_recent(limit=10)
    assert len(entries) == 4
    assert entries[0].level == "debug"
    assert entries[1].level == "info"
    assert entries[2].level == "warn"
    assert entries[3].level == "error"


def test_log_buffer_max_size_eviction():
    """Verify oldest entries are evicted when buffer is full."""
    buffer = LogBuffer(max_size=3)
    buffer.info("Entry 1")
    buffer.info("Entry 2")
    buffer.info("Entry 3")
    buffer.info("Entry 4")  # Should evict Entry 1

    entries = buffer.get_recent(limit=10)
    assert len(entries) == 3
    assert entries[0].message == "Entry 2"
    assert entries[1].message == "Entry 3"
    assert entries[2].message == "Entry 4"


def test_log_buffer_filter_by_level():
    """Verify level filtering in get_recent()."""
    buffer = LogBuffer(max_size=10)
    buffer.debug("Debug 1")
    buffer.info("Info 1")
    buffer.warn("Warn 1")
    buffer.error("Error 1")
    buffer.info("Info 2")

    info_entries = buffer.get_recent(limit=10, level="info")
    assert len(info_entries) == 2
    assert all(e.level == "info" for e in info_entries)

    error_entries = buffer.get_recent(limit=10, level="error")
    assert len(error_entries) == 1
    assert error_entries[0].message == "Error 1"


def test_log_buffer_filter_by_time():
    """Verify time-based filtering in get_recent()."""
    buffer = LogBuffer(max_size=10)

    # Create entries with timestamps 5 minutes apart
    now = datetime.now(UTC)
    cutoff_time = (now - timedelta(minutes=2)).isoformat()

    buffer.info("Old entry")
    # Manually adjust timestamp for testing
    buffer._buffer[-1].timestamp = (now - timedelta(minutes=5)).isoformat()

    buffer.info("Recent entry")
    # This entry timestamp is generated by log() and will be current

    recent_entries = buffer.get_recent(limit=10, since=cutoff_time)
    assert len(recent_entries) == 1
    assert recent_entries[0].message == "Recent entry"


def test_log_buffer_limit():
    """Verify limit parameter restricts number of returned entries."""
    buffer = LogBuffer(max_size=100)
    for i in range(20):
        buffer.info(f"Entry {i}")

    entries = buffer.get_recent(limit=5)
    assert len(entries) == 5
    # Should return most recent 5 entries
    assert entries[0].message == "Entry 15"
    assert entries[4].message == "Entry 19"


def test_log_buffer_clear():
    """Verify clear() removes all entries."""
    buffer = LogBuffer(max_size=10)
    buffer.info("Entry 1")
    buffer.info("Entry 2")
    buffer.info("Entry 3")

    assert buffer.size() == 3
    buffer.clear()
    assert buffer.size() == 0


def test_log_buffer_size():
    """Verify size() returns correct count."""
    buffer = LogBuffer(max_size=10)
    assert buffer.size() == 0

    buffer.info("Entry 1")
    assert buffer.size() == 1

    buffer.info("Entry 2")
    buffer.info("Entry 3")
    assert buffer.size() == 3


def test_log_buffer_timestamps():
    """Verify timestamps are ISO 8601 with timezone."""
    buffer = LogBuffer(max_size=10)
    buffer.info("Test entry")

    entries = buffer.get_recent(limit=1)
    timestamp = entries[0].timestamp

    # Should parse as ISO 8601
    parsed = datetime.fromisoformat(timestamp)
    assert parsed.tzinfo is not None  # Timezone must be present


